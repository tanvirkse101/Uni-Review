function hover(x) {
    x.style.border = "5px";
    x.style.borderStyle = "solid";
    x.style.borderRadius =  "50px";
    x.style.borderColor = "red";
}


function endHover (x) {
    x.style.border = "5px";
    x.style.borderStyle = "solid";
    x.style.borderRadius =  "15px";
    x.style.borderColor = "rgb(65, 22, 221)";
}